package org.bradylab.plates.web.model;


public class WebDataPoint {

	private String id;
	private String x;
	private String y;
	private String z;

	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public String getX() {
		return x;
	}

	public void setX(String x) {
		this.x = x;
	}

	public String getY() {
		return y;
	}

	public void setY(String y) {
		this.y = y;
	}
	
	public String getZ() {
		return z;
	}
	
	public void setZ(String z) {
		this.z = z;
	}
}
